#include <stdlib.h>
#include <stdio.h>

int		ft_sqrt(int nb)
{
	long i;

	i = 0;
	if (nb == 1)
		return (1);
	while (i <= nb / 2)
	{
		if (i * i == nb)
			return ((int)i);
		i++;
	}
	return (0);
}
