int		ft_str_is_alpha(char *str)
{
	int	i;

	i = 0;
	while (str[i])
	{
		if ((str[i] > 0 && str[i] < 65) || (str[i] > 90 && str[i] < 97)
				|| (str[i] > 122 && str[i] < 127))
			return (0);
		else
			i++;
	}
	return (1);
}
